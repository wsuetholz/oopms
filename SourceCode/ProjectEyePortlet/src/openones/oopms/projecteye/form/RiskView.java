package openones.oopms.projecteye.form;

public class RiskView {
	private String riskId;
	private String riskSource;
    private String probability;
    private String priority;
    private String estimatedImpact;
    private String description;
    private String trigger;
	/**
	 * @return the riskId
	 */
	public String getRiskId() {
		return riskId;
	}
	/**
	 * @param riskId the riskId to set
	 */
	public void setRiskId(String riskId) {
		this.riskId = riskId;
	}
	/**
	 * @return the riskSource
	 */
	public String getRiskSource() {
		return riskSource;
	}
	/**
	 * @param riskSource the riskSource to set
	 */
	public void setRiskSource(String riskSource) {
		this.riskSource = riskSource;
	}
	/**
	 * @return the probability
	 */
	public String getProbability() {
		return probability;
	}
	/**
	 * @param probability the probability to set
	 */
	public void setProbability(String probability) {
		this.probability = probability;
	}
	/**
	 * @return the priority
	 */
	public String getPriority() {
		return priority;
	}
	/**
	 * @param priority the priority to set
	 */
	public void setPriority(String priority) {
		this.priority = priority;
	}
	/**
	 * @return the estimatedImpact
	 */
	public String getEstimatedImpact() {
		return estimatedImpact;
	}
	/**
	 * @param estimatedImpact the estimatedImpact to set
	 */
	public void setEstimatedImpact(String estimatedImpact) {
		this.estimatedImpact = estimatedImpact;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the trigger
	 */
	public String getTrigger() {
		return trigger;
	}
	/**
	 * @param trigger the trigger to set
	 */
	public void setTrigger(String trigger) {
		this.trigger = trigger;
	}
    
    
}
