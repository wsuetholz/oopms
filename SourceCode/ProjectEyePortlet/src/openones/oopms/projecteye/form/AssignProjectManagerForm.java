/**
 * @author HaiTCT
 */
public class AssignProjectManagerForm {
	private String userId;
	private String userName;
	private String userAccount;
	private String userRoleString;
	private String userRole;

	/**
	 * @return the userRoleString
	 */
	public String getUserRoleString() {
		return userRoleString;
	}

	/**
	 * @param userRoleString
	 *            the userRoleString to set
	 */
	public void setUserRoleString(String userRoleString) {
		this.userRoleString = userRoleString;
	}


	/**
	 * @return the userId
	 */
	public String getUserId() {
		return userId;
	}

	/**
	 * @param userId
	 *            the userId to set
	 */
	public void setUserId(String userId) {
		this.userId = userId;
	}

	/**
	 * @return the userName
	 */
	public String getUserName() {
		return userName;
	}

	/**
	 * @param userName
	 *            the userName to set
	 */
	public void setUserName(String userName) {
		this.userName = userName;
	}

	/**
	 * @return the userAccount
	 */
	public String getUserAccount() {
		return userAccount;
	}

	/**
	 * @param userAccount
	 *            the userAccount to set
	 */
	public void setUserAccount(String userAccount) {
		this.userAccount = userAccount;
	}

	/**
	 * @return the userRole
	 */
	public String getUserRole() {
		return userRole;
	}

	/**
	 * @param userRole
	 *            the userRole to set
	 */
	public void setUserRole(String userRole) {
		this.userRole = userRole;
	}

}
